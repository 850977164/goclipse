package com.googlecode.goclipse.ui.util;

import org.eclipse.jface.text.TextAttribute;
import org.eclipse.jface.text.rules.RuleBasedScanner;
import org.eclipse.jface.text.rules.Token;
import org.eclipse.swt.graphics.Color;

public class SingleTokenScanner extends RuleBasedScanner {
	
	public SingleTokenScanner(Color fgColor, int style) {
		Token defaultToken = new Token(new TextAttribute(fgColor, null, style));
		
		setDefaultReturnToken(defaultToken);
	}
	
}